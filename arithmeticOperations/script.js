let n1 = Number(prompt("Enter number 1"));
let n2 = Number(prompt("Enter number 2"));

function arithmetic(n1 , n2)
{
   document.write(n1+n2);
   document.write("<br>");
   document.write(n1-n2);
   document.write("<br>");
   document.write(n1*n2);
   document.write("<br>");
   document.write(n1/n2);

}

arithmetic(n1 , n2);